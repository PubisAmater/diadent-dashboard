// SVG info icon
function _tipIcon() {
  return '<svg class="tip-ic" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round"><circle cx="12" cy="12" r="10"/><line x1="12" y1="16" x2="12" y2="12"/><line x1="12" y1="8" x2="12.01" y2="8"/></svg>';
}

// Inject tooltip icons into matching label elements
function injectTooltips() {
  // Selectors to scan
  const selectors = ['.ml', '.fc-l', '.nc-pl', '.sil', '.coef-lbl', '.erow-m', '.drill-label', '.dp-section'];
  selectors.forEach(sel => {
    document.querySelectorAll(sel).forEach(el => {
      // Skip if already has a tip
      if (el.querySelector('.tip')) return;
      const text = el.textContent.trim();
      const key = Object.keys(TIP_MAP).find(k => text === k || text.startsWith(k));
      if (!key) return;
      const tip = document.createElement('span');
      tip.className = 'tip';
      tip.innerHTML = _tipIcon() + '<span class="tip-box">' + TIP_MAP[key] + '</span>';
      el.appendChild(tip);
    });
  });
}

document.addEventListener('DOMContentLoaded', injectTooltips);
// Re-run when doctor view renders dynamically
document.addEventListener('doctorRendered', injectTooltips);
